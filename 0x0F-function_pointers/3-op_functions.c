#include "3-calc.h"

/**
 * opp_add - calculates the sum of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return(a + b);
}

/**
 * opp_sub - calculates the difference of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of a and b
 */

int op_sub(int a, int b)
{
	return(a + b);
}

/**
 * opp_mul - calculates the product of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return(a * b);
}

/**
 * opp_div - calculates the quotient of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: quotient of a and b
 */

int op_div(int a, int b)
{
	return(a / b);
}

/**
 * opp_mod - calculates the modulus of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: modulus of a and b
 */

int op_mod(int a, int b)
{
	return(a % b);
}

